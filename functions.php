function ui_scripts() {
	wp_enqueue_style( 'bootstrap', get_template_directory_uri() . '/style/css/bootstrap.min.css', 1);
	wp_enqueue_style( 'web-elegant-icons', get_template_directory_uri() . '/style/css/elegant-icons.min.css' );
